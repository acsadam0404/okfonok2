	MimeType(String mimeType, String extension) {
		this.mimeType = mimeType;
		this.extension = extension;

	}


	public String getMimeType() {
		return mimeType;
	}


	public String getExtension() {
		return extension;
	}


	public static MimeType get(String str) {
		try {
			return valueOf(unpunctuate(str));
		}
		catch (Exception e) {
			return null;
		}
	}


	public static List<String> getImageTypes() {
		List<String> imageTypes = new ArrayList<>();
		for (MimeType mt : values()) {
			if (mt.getMimeType().startsWith("image")) {
				imageTypes.add(mt.getMimeType());
			}
		}
		return imageTypes;
	}


	public static MimeType getByExtension(String extension) {
		for (MimeType mt : values()) {
			if (mt.getExtension().equals(extension)) {
				return mt;
			}
		}
		return unknown;
	}


	private static String unpunctuate(String str) {
		return str.replaceAll("[-/_+.,;]", "_");
	}
